<ul>
<li>We collect information you provide about your use of and satisfaction with OCW through email you send us, through the OCW feedback form, and through OCW surveys, whether or not you voluntarily include your contact information.</li>
<li>We may use web analysis tools that are built into the OCW web site to measure and collect anonymous session information.</li>
<li>We also use "cookies" to improve your OCW web experience and to collect anonymous information about how you use OCW. However, cookies are not required for OCW use. If your browser is configured not to accept cookies, you will still be able to access OCW and its content.</li>
</ul>
<br />
When we report information about OCW access, use, and impact, we report aggregate, non-personally identifiable data. Occasionally, we report quoted feedback from users. We do not attribute feedback to specific individuals unless we obtain permission to use that person's name along with the feedback.<br />


