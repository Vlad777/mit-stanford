<b>Privacy</b><br />
MIT OpenCourseWare respects your privacy. We do not collect personally identifiable information about you unless you voluntarily provide it, such as when you provide email contact information to subscribe to the OCW newsletter, send feedback to OCW, or respond to a survey. If you voluntarily provide your email address or other contact information, we might also use it to inform you of changes to OCW, to survey you about your use or opinion of OCW, or to ask for your support. At your request, we will remove your contact information from our files.<br />

We do not make your contact information or any other personally identifiable information available to anyone outside OCW or its service providers (who use the information only for authorized OCW purposes) unless we are legally required to do so.<br />

